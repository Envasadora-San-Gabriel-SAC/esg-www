<h1>TÉRMINOS Y CONDICIONES</h1>

<p>En cumplimiento de lo dispuesto por la Ley N° 29733, Ley de Protección de
Datos Personales, y su Reglamento, los datos personales consignados en
cada formulario se quedarán incorporados en las bases de datos de ESG;
autorizando expresamente a que los mismos sean tratados con la finalidad de
contactarse con sus usuarios y ofrecerles contenidos vinculados a nuestros
servicios.</p>
<p>ESG cumple con la normativa vigente en materia de protección de datos
personales y, en particular, con las medidas de seguridad correspondiente; por
tal motivo, utiliza los datos brindados por los usuarios única y exclusivamente
para los fines establecidos, guardando confidencialidad respecto a todos los
datos de carácter personal que conozca y a los que tenga acceso.</p>
<p>Igualmente, ESG custodia e impide el acceso a los datos de carácter personal
a cualquier tercero ajeno; garantizando que los usuarios registrados ejerzan, de
ser el caso, los derechos de acceso, rectificación, cancelación, y oposición de
sus datos personales, solicitándolo por correo electrónico a la siguiente
dirección: soporte@esg.com.pe</p>